import math
#指數
number = 123
number = number ** 3
print(number)

print('\n')

#次方 pow
print('pow(2, 3):',pow(2, 3))

print('\n')

#max and min
numbers = [1, 5, 8, 2, 9]
max_number = max(numbers)
min_number = min(numbers)
print('Max:', max_number)
print('Min:', min_number)

print('\n')

#round
number = 3.14159
rounded_number = round(number, 2)
print('Rounded number:', rounded_number)

print('\n')

#abs
number = -5
absolute_number = abs(number)
print('Absolute number:', absolute_number)

print('\n')

#ceil and floor
number = 3.14159
ceil_number = math.ceil(number)
floor_number = math.floor(number)
print('Ceil number:', ceil_number)
print('Floor number:', floor_number)

print('\n')

#PI
pi = math.pi
print('π:', pi)

print('\n')