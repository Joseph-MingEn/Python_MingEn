# 字符串的長度
my_string = "Hello, World!"
length_of_string = len(my_string)
print("字符串長度:", length_of_string)

# 列表的長度
my_list = [1, 2, 3, 4, 5]
length_of_list = len(my_list)
print("列表長度:", length_of_list)

# 元組的長度
my_tuple = (1, 2, 3, 4, 5)
length_of_tuple = len(my_tuple)
print("元組長度:", length_of_tuple)